/* 
    Ejercicio 5: Números pares
    Objetivo:  Mostrar números pares del 2 al 20 usando un bucle while.
*/

let numeroPar = 2; // Inicio

console.log('Números Pares (2 al 20');

// Muestra los números pares del 2 al 20
while (numeroPar <= 20) {
  console.log(numeroPar);
  numeroPar += 2; // Incrementa de 2 en 2
}
